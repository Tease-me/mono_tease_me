export function filterCountries(query: string, limit = 12): string[] {
  const q = query.trim().toLowerCase();
  if (!q) return COUNTRY_NAMES.slice(0, limit);
  return COUNTRY_NAMES.filter((name) => name.toLowerCase().includes(q)).slice(0, limit);
}

export function isKnownCountry(value: string): boolean {
  const trimmed = value.trim();
  if (!trimmed) return false;
  return COUNTRY_NAMES.some((c) => c.toLowerCase() === trimmed.toLowerCase());
}
